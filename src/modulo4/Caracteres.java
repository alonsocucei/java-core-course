
public class Caracteres {
    public static void main(String[] args) {
        char arroba; //declaracion de variable
        arroba = '\u221B';//asignacion

        System.out.println(arroba);
        
        //Impresion de literales tipo char
        //sintaxis unicode: ' \ u_ _ _ _' ( _ digito en hexadecimal)
        System.out.println('\u0040');
        System.out.println('@');
        
        System.out.println('\u2219');
        System.out.println('\u221B');
        System.out.println('\u222B');
        
    }
}

package modulo4;
public class BasesNumericas {
    public static void main(String[] args) {
        int numeroDecimal = 10;
        int numeroBinario = 0b1010;
        int numeroHexadecimal = 0xA;
        int numeroOctal = 012;
        double notacionCientifica = 1e1;
        
        System.out.println(numeroDecimal);
        System.out.println(numeroBinario);
        System.out.println(numeroHexadecimal);
        System.out.println(numeroOctal);
        System.out.println(notacionCientifica);
    }
}

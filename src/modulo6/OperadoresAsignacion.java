
public class OperadoresAsignacion {
    public static void main(String[] args) {
        int a = 1;
        
        a = a + 1;
        System.out.println("El valor despues de a = a + 1: " + a);
        
        a += 10;
        System.out.println("El valor despues de a += 10: " + a);
        
        a = a * 10;
        System.out.println("El valor despues de a = a * 10: " + a);
        
        a *= 10;
        System.out.println("El valor despues de a *= 10: " + a);
        
        a ++;
        System.out.println("El valor despues de a++: " + a);
        
    }
}
